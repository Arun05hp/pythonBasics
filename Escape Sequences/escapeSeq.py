# Escape Sequences

# \"
# \'
# \\
# \n newLine





course = "python \"Programming"

print(course)
# o/p : python "Programming

course = "python \'Programming"

print(course)
# o/p : python 'Programming

course = "python \\Programming"

print(course)
# o/p : python \Programming

course = "python \nProgramming"

print(course)
# o/p : python
#       Programming